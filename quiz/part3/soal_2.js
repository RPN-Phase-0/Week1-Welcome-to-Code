// Looping Menggunakan For Loop

// Looping Pertama

console.log('LOOPING PERTAMA \n');

for (let i = 0; i < 10; i++) {
	console.log(`${i} - Caffein > Soda`);
}

// KASIH SPACE SIRR
console.log('');

// Looping kedua

console.log('LOOPING KEDUA \n');

for (let j = 10; j > 0; j--) {
	console.log(`${j} - Soda < Caffein`);
}
